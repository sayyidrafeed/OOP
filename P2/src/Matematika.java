class Matematika {
    private double hasil;

    public double jumlah(double a, double b) {
        hasil = a + b;
        return hasil;
    }

    public double kurang(double a, double b) {
        hasil = a - b;
        return hasil;
    }

    public double kali(double a, double b) {
        hasil = a * b;
        return hasil;
    }

    public double bagi(double a, double b) {
        hasil = a / b;
        return hasil;
    }
}